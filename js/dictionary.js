// ─── HIGHLIGHT ───────────────────────────────────────────────────────────────
function highlight(text, q) {
    if (!q) return text;
    const re = new RegExp(`(${q.replace(/[.*+?^${}()|[\]\\]/g, '\\$&')})`, 'gi');
    return text.replace(re, '<mark>$1</mark>');
}

// ─── FILTER LOGIC ────────────────────────────────────────────────────────────
function getFiltered() {
    return state.dictionary.filter(e => {
        const q = state.query;
        const matchQ = !q ||
            e.word.toLowerCase().includes(q) ||
            e.definition.toLowerCase().includes(q) ||
            e.pos.toLowerCase().includes(q);
        const matchP = state.filterPos === 'all' || e.pos === state.filterPos;
        const matchA = state.filterAlpha === 'all' || e.letter === state.filterAlpha;
        return matchQ && matchP && matchA;
    });
}

// ─── RENDER ──────────────────────────────────────────────────────────────────
function render() {
    if (state.dictionary.length === 0) {
        return;
    }

    const filtered = getFiltered();
    const q = state.query;

    // Results info
    if (q || state.filterPos !== 'all' || state.filterAlpha !== 'all') {
        resultsInfo.innerHTML = `
            Menampilkan <strong>${filtered.length}</strong> dari ${state.dictionary.length} entri
            ${q ? `· hasil untuk "<em>${q}</em>"` : ''}
            <button class="clear-btn" id="clearBtn">Bersihkan filter</button>
        `;
        document.getElementById('clearBtn')?.addEventListener('click', clearAll);
    } else {
        resultsInfo.innerHTML = `<strong>${state.dictionary.length}</strong> entri tersedia`;
    }

    if (filtered.length === 0) {
        dictContainer.innerHTML = `
            <div class="empty-state">
                <div class="icon">📭</div>
                <h3>Kata tidak ditemukan</h3>
                <p>Coba dengan ejaan berbeda atau cari kata lain yang ingin Anda ketahui.</p>
            </div>
        `;
        return;
    }

    // Group by letter
    const groups = {};
    filtered.forEach(e => {
        if (!groups[e.letter]) groups[e.letter] = [];
        groups[e.letter].push(e);
    });

    let html = '';
    Object.keys(groups).sort().forEach(letter => {
        const items = groups[letter];
        html += `
            <div class="letter-section" id="sec-${letter}">
                <div class="letter-heading" data-count="${items.length}">${letter}</div>
                <div class="entries-grid">
                    ${items.map(e => renderCard(e, q)).join('')}
                </div>
            </div>
        `;
    });

    dictContainer.innerHTML = html;

    // Attach card click
    dictContainer.querySelectorAll('.entry-card').forEach(card => {
        card.addEventListener('click', () => {
            const idx = parseInt(card.dataset.idx);
            openModal(filtered[idx]);
        });
    });
}

function renderCard(e, q) {
    const wordHl = highlight(e.word, q);
    const defHl = highlight(e.definition, q);
    const idx = getFiltered().indexOf(e);
    return `
        <div class="entry-card" data-idx="${idx}" role="button" tabindex="0" 
             aria-label="${e.word}: ${e.definition}">
            <div class="entry-word">${wordHl}</div>
            <div class="entry-meta">
                ${e.pos ? `<span class="tag tag-pos">${e.pos}</span>` : ''}
                ${e.dialect ? `<span class="tag tag-dialect">${e.dialect}</span>` : ''}
            </div>
            <div class="entry-definition">${defHl}</div>
        </div>
    `;
}

